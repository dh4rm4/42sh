#include "shell.h"

int		ft_reset_line(t_env *e)
{
	if (e->line)
	{
		free(e->line);
		e->line = NULL;
		e->tcaps.nb_move = 0;
		e->tcaps.nb_read = 0;
	}
	return (0);
}

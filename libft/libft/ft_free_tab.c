#include "libft.h"

void		ft_free_tab(char **tab)
{
	int		i;

	i = 0;
	if (tab != NULL)
	{
		while (tab[i] != NULL)
		{
			free(tab[i]);
			++i;
		}
		free(tab);
	}
}

#include "libft.h"

int		ft_atoi(const char *str)
{
	int		n;
	int		i;
	int		sign;

	n = 0;
	i = 0;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t' || str[i] == '\r' ||
			str[i] == '\f' || str[i] == '\v')
		++i;
	sign = (str[i] == '-') ? -1 : 1;
	if (str[i] == '+' || str[i] == '-')
		++i;
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = n * 10 + (str[i] - 48) % 10;
		++i;
	}
	return (n * sign);
}
